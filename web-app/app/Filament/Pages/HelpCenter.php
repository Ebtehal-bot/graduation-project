    }

    public function toggleFaq(string $faqId): void
    {
        $this->activeFaq = $this->activeFaq === $faqId ? null : $faqId;
    }

    public function getFilteredModules(): array
    {
        $all = $this->getAllModules();
        if (empty($this->search)) {
            return $all;
        }
        $q = trim($this->search);
        return array_filter($all, function ($m) use ($q) {
            if (mb_stripos($m['title'], $q) !== false) {
                return true;
            }
            if (mb_stripos($m['description'], $q) !== false) {
                return true;
            }
            foreach ($m['faq'] ?? [] as $f) {
                if (mb_stripos($f['q'], $q) !== false || mb_stripos($f['a'], $q) !== false) {
                    return true;
                }
            }
            foreach ($m['details'] ?? [] as $d) {
                if (mb_stripos($d['title'], $q) !== false || mb_stripos($d['body'], $q) !== false) {
                    return true;
                }
            }
            return false;
        });
    }

    public function getAllFaqs(): array
    {
        $faqs = [];
        foreach ($this->getAllModules() as $id => $m) {
            foreach ($m['faq'] ?? [] as $f) {
                $faqs[] = ['id' => $id . '_' . count($faqs), 'q' => $f['q'], 'a' => $f['a']];
            }
        }
        return $faqs;
    }

